
package Texas_Hold_Em;

// This package provides classes necessary for implementing a game system for playing poker


public class FourOfAKind extends TexasHand {
	
	//--------------------------------------------------------------------//
	//--------------------------------------------------------------------//
	// Constructors
	//--------------------------------------------------------------------//
	//--------------------------------------------------------------------//
	
	public FourOfAKind(Card[] hand, DeckOfCards deck) {
		super(hand, deck);
	}


	//--------------------------------------------------------------------//
	//--------------------------------------------------------------------//
	// What is the riskworthiness of this hand?
	//--------------------------------------------------------------------//
	//--------------------------------------------------------------------//
	
	public int getRiskWorthiness() 	{
		return 100 - TexasHand.FOURS_RISK;
	}

	
	//--------------------------------------------------------------------//
	//--------------------------------------------------------------------//
	// What is the value of this hand?
	//--------------------------------------------------------------------//
	//--------------------------------------------------------------------//
	
	public int getValue()	{
		if (getCard(0).getRank() == getCard(3).getRank())
			return TexasHand.FOURS_VALUE + getCard(0).getValue();
		else
			return TexasHand.FOURS_VALUE + getCard(1).getValue();
	}
	

	//--------------------------------------------------------------------//
    //--------------------------------------------------------------------//
	// Discard and redeal some cards
	//--------------------------------------------------------------------//
	//--------------------------------------------------------------------//
	
	public TexasHand discard()	{
		if (getCard(0).getRank() == getCard(3).getRank())
			return discard(4);
		else
			return discard(0);
	}

	//--------------------------------------------------------------------//
	//--------------------------------------------------------------------//
	// Display
	//--------------------------------------------------------------------//
	//--------------------------------------------------------------------//
	
	public String toString() 	{
		return "Four of a kind: " + super.toString();
	}
	
}

